    public static java.util.List<java.lang.Integer> getResIDs(org.secuso.privacyfriendlymemory.model.CardDesign cardDesign, org.secuso.privacyfriendlymemory.model.MemoGameDifficulty memoryDifficulty, boolean shuffled) {
        java.util.List<java.lang.Integer> imagesForCardDesign = new java.util.LinkedList<>();
        switch (cardDesign) {
            case FIRST :
                imagesForCardDesign = org.secuso.privacyfriendlymemory.model.MemoGameDefaultImages.images1;
                break;
            case SECOND :
                imagesForCardDesign = org.secuso.privacyfriendlymemory.model.MemoGameDefaultImages.images2;
                break;
        }
        if (shuffled) {
            java.util.Collections.shuffle(imagesForCardDesign);
        }
        return org.secuso.privacyfriendlymemory.model.MemoGameDefaultImages.reduce(memoryDifficulty, imagesForCardDesign);
    }

    private static java.util.List<java.lang.Integer> reduce(org.secuso.privacyfriendlymemory.model.MemoGameDifficulty memoryDifficulty, java.util.List<java.lang.Integer> images) {
        int differentImages = (memoryDifficulty.getDeckSize()) / 2;
        if (differentImages > (images.size())) {
            throw new java.lang.IllegalStateException("Requested deck contains not enough images for the specific game difficulty");
        }
        return images.subList(0, differentImages);
    }
